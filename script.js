const categoryLabels = {
  housing: "집과 임대차",
  work: "일터와 임금",
  contract: "계약과 돈",
  family: "가족과 상속",
  consumer: "소비자 분쟁"
};

const pageSize = 6;
let activeTopic = "all";
let currentPage = 1;
let searchTerm = "";

const postGrid = document.querySelector("#postGrid");
const pageNumbers = document.querySelector("#pageNumbers");
const prevPage = document.querySelector("#prevPage");
const nextPage = document.querySelector("#nextPage");
const searchInput = document.querySelector("#searchInput");
const topicButtons = document.querySelectorAll(".topic");
let postDetail = document.querySelector("#postDetail");

if (!postDetail) {
  postDetail = document.createElement("article");
  postDetail.className = "post-detail";
  postDetail.id = "postDetail";
  postDetail.setAttribute("aria-live", "polite");
  postDetail.hidden = true;
  document.querySelector("#posts").after(postDetail);
}

function filteredPosts() {
  return posts.filter((post) => {
    const matchesTopic = activeTopic === "all" || post.category === activeTopic;
    const haystack = `${post.title} ${categoryLabels[post.category]} ${post.excerpt}`.toLowerCase();
    return matchesTopic && haystack.includes(searchTerm.toLowerCase());
  });
}

function renderPosts() {
  const visiblePosts = filteredPosts();
  const pageCount = Math.max(1, Math.ceil(visiblePosts.length / pageSize));
  currentPage = Math.min(currentPage, pageCount);
  const start = (currentPage - 1) * pageSize;
  const pagePosts = visiblePosts.slice(start, start + pageSize);

  postGrid.innerHTML = pagePosts.map((post) => `
    <article class="post-card">
      <button class="post-image post-open" type="button" data-slug="${post.slug}" style="background-image: url('${post.image}')" aria-label="${post.title} 본문 열기"></button>
      <div class="post-body">
        <div class="post-meta">
          <span class="post-tag">${categoryLabels[post.category]}</span>
          <span>${post.date}</span>
        </div>
        <h3>
          <button class="post-title-button post-open" type="button" data-slug="${post.slug}">${post.title}</button>
        </h3>
        <p>${post.excerpt}</p>
        <button class="read-link post-open" type="button" data-slug="${post.slug}" aria-label="${post.title} 읽기">자세히 읽기</button>
      </div>
    </article>
  `).join("");

  if (pagePosts.length === 0) {
    postGrid.innerHTML = `<p class="empty-state">검색 결과가 없습니다. 다른 생활법률 키워드로 찾아보세요.</p>`;
  }

  renderPagination(pageCount);
}

function renderPagination(pageCount) {
  prevPage.disabled = currentPage === 1;
  nextPage.disabled = currentPage === pageCount;

  pageNumbers.innerHTML = Array.from({ length: pageCount }, (_, index) => {
    const page = index + 1;
    const active = page === currentPage ? " active" : "";
    return `<button class="page-number${active}" type="button" data-page="${page}" aria-label="${page}페이지">${page}</button>`;
  }).join("");
}

function renderDetail(post) {
  postDetail.hidden = false;
  postDetail.innerHTML = `
    <button class="detail-back" type="button">글 목록으로 돌아가기</button>
    <div class="detail-hero" style="background-image: url('${post.image}')"></div>
    <div class="detail-body">
      <div class="post-meta">
        <span class="post-tag">${categoryLabels[post.category]}</span>
        <span>${post.date}</span>
      </div>
      <h2>${post.title}</h2>
      <p class="detail-lead">${post.excerpt}</p>
      ${post.body.map((section) => `
        <section class="detail-section">
          <h3>${section.heading}</h3>
          ${section.paragraphs.map((paragraph) => `<p>${paragraph}</p>`).join("")}
        </section>
      `).join("")}
      <aside class="detail-note">
        이 글은 생활법률 이해를 돕기 위한 일반 정보입니다. 실제 사건은 계약서, 날짜, 증거, 당사자 관계에 따라 결론이 달라질 수 있습니다.
      </aside>
      <div class="detail-bottom-actions">
        <button class="detail-back detail-back-bottom" type="button">글 목록으로 돌아가기</button>
      </div>
    </div>
  `;
  postDetail.querySelectorAll(".detail-back").forEach((button) => {
    button.addEventListener("click", closeDetail);
  });
  window.scrollTo({ top: Math.max(postDetail.offsetTop - 90, 0), behavior: "auto" });
}

function openDetail(slug) {
  const post = posts.find((item) => item.slug === slug);
  if (!post) return;
  renderDetail(post);
  history.replaceState(null, "", `#${slug}`);
}

function closeDetail() {
  postDetail.hidden = true;
  postDetail.innerHTML = "";
  history.replaceState(null, "", "#posts");
  document.querySelector("#posts").scrollIntoView({ behavior: "smooth", block: "start" });
}

topicButtons.forEach((button) => {
  button.addEventListener("click", () => {
    topicButtons.forEach((item) => item.classList.remove("active"));
    button.classList.add("active");
    activeTopic = button.dataset.topic;
    currentPage = 1;
    renderPosts();
  });
});

searchInput.addEventListener("input", (event) => {
  searchTerm = event.target.value.trim();
  currentPage = 1;
  renderPosts();
});

postGrid.addEventListener("click", (event) => {
  const opener = event.target.closest(".post-open");
  if (!opener) return;
  openDetail(opener.dataset.slug);
});

pageNumbers.addEventListener("click", (event) => {
  const button = event.target.closest("button[data-page]");
  if (!button) return;
  currentPage = Number(button.dataset.page);
  renderPosts();
  document.querySelector("#posts").scrollIntoView({ behavior: "smooth", block: "start" });
});

prevPage.addEventListener("click", () => {
  currentPage -= 1;
  renderPosts();
});

nextPage.addEventListener("click", () => {
  currentPage += 1;
  renderPosts();
});

renderPosts();

const initialSlug = window.location.hash.replace("#", "");
if (initialSlug) {
  openDetail(initialSlug);
}
